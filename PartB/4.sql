SET SERVEROUTPUT ON

DECLARE 
	c_id employee.ssn%type;
	c_name employee.name%type;
	c_salary employee.salary%type;
	c_deptno employee.deptno%type;
	
	CURSOR c1
		IS SELECT ssn,name,salary,deptno from employee;
	
BEGIN
	OPEN c1;
	LOOP
		FETCH c1 into c_id,c_name,c_salary,c_deptno;
		EXIT WHEN c1%notfound;
		INSERT INTO employee_copy VALUES (c_id,c_name,c_salary,c_deptno);
	END LOOP;
	CLOSE c1;
	dbms_output.put_line("Table copied successfully");
END;
/



































































































































































































